package com.free.platform.paginator.exception;

public class BuilderExportFileException extends Exception {

	private static final long serialVersionUID = 3124358689421975124L;

	public BuilderExportFileException() {
		super();
	}

	public BuilderExportFileException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BuilderExportFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public BuilderExportFileException(String message) {
		super(message);
	}

	public BuilderExportFileException(Throwable cause) {
		super(cause);
	}
}
